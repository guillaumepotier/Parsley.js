// Load this after Parsley for additional comparison validators

window.Parsley.addValidator('dateiso', {
  validateString: function (value) {
    return /^(?:[1-9]\d{3}-(?:(?:0[1-9]|1[0-2])-(?:0[1-9]|1\d|2[0-8])|(?:0[13-9]|1[0-2])-(?:29|30)|(?:0[13578]|1[02])-31)|(?:[1-9]\d(?:0[48]|[2468][048]|[13579][26])|(?:[2468][048]|[13579][26])00)-02-29)$/.test(value);
  },
  priority: 256
});
